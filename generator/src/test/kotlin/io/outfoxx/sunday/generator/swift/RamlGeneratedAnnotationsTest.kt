package io.outfoxx.sunday.generator.swift

import io.outfoxx.sunday.generator.GenerationMode.Client
import io.outfoxx.sunday.generator.swift.SwiftTypeRegistry.Option.AddGeneratedAnnotation
import io.outfoxx.sunday.generator.swift.tools.findType
import io.outfoxx.sunday.generator.swift.tools.generate
import io.outfoxx.sunday.generator.swift.tools.generateTypes
import io.outfoxx.sunday.test.extensions.ResourceExtension
import io.outfoxx.sunday.test.extensions.ResourceUri
import org.junit.jupiter.api.Assertions.assertTrue
import org.junit.jupiter.api.DisplayName
import org.junit.jupiter.api.Test
import org.junit.jupiter.api.extension.ExtendWith
import java.net.URI

@ExtendWith(ResourceExtension::class)
@DisplayName("[Swift] [RAML] Generated Annotations Test")
class RamlGeneratedAnnotationsTest {

  @Test
  fun `test generated annotation is added to root classes`(
    @ResourceUri("raml/type-gen/general/generated-annotations.raml") testUri: URI
  ) {

    val typeRegistry = SwiftTypeRegistry(Client, setOf(AddGeneratedAnnotation))

    val type = findType("Test", generateTypes(testUri, typeRegistry))

    assertTrue(type.toString().contains("Generated By:"))
    assertTrue(type.toString().contains("Generated On:"))
  }

  @Test
  fun `test generated annotation is added to service class`(
    @ResourceUri("raml/type-gen/general/generated-annotations.raml") testUri: URI
  ) {

    val typeRegistry = SwiftTypeRegistry(Client, setOf(AddGeneratedAnnotation))

    val builtTypes =
      generate(testUri, typeRegistry) { document ->
        SwiftSundayGenerator(
          document,
          typeRegistry,
          "http://example.com/",
          listOf("application/json")
        )
      }

    val type = findType("API", builtTypes)

    assertTrue(type.toString().contains("Generated By:"))
    assertTrue(type.toString().contains("Generated On:"))
  }
}
